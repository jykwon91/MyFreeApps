def _extract_domain(email: str) -> str | None:
    """Lowercase the bit after ``@``. Returns None if the input isn't an email.

    Pure helper — no validation beyond ``@``-presence. The
    ``email-validator`` step earlier in the pipeline owns format checking.
    """
    if not email or "@" not in email:
        return None
    domain = email.rsplit("@", 1)[-1].strip().lower()
    return domain or None


def is_disposable_email(email: str) -> bool:
    """True iff the email's domain is in the blocklist (case-insensitive)."""
    domain = _extract_domain(email)
    if domain is None:
        return False
    return domain in DISPOSABLE_EMAIL_DOMAINS


